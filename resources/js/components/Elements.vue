import Table from "./Table.vue";
import Button from "./Button.vue";
import TableGraph from "./TableGraph.vue";
export default {
  name: "Elements",
  props: ["type", "count", 'element', 'elementId'],
  data: () => ({
    isOpenTitle: false,
    isOpenSubTitle: false,
    isOpenTextArea: false,
    isActive: false,
    isActiveImage: false,
    isOpenImage: false,
    isOpenPieCharts: false,
    isActiveList: false,
    isActiveTable: false,
    isActivePersTable: false,
    isActiveButton: false,
    global: true,
    isOpenListUl: false,
    isOpenListOl: false,
    isOpenListImage: false,
    isOpenTable: false,
    isOpenButton: false,
    isOpenTableGraph: false,
    isActiveGraph: false,
    tr: 0,
    td: 0,
    tagname: {
      isOpenTitle: 'h1',
      isOpenSubTitle: 'h3',
      isOpenTextArea: 'text',
      isOpenImage: 'img',
      isOpenPieCharts: 'graph',
      isOpenTable: 'table',
      isOpenListUl: 'listMarc',
      isOpenListOl: 'listNumeric',
      isOpenListImage: 'listIcon',
      isOpenButton: 'btn',
      isOpenTableGraph: 'tableGraph'
    },
    curElement: null,
    activeType: '',
  }),
  beforeMount() {
    this.curElement = this.element
    for (const key in this.tagname) {
      if (this.tagname[key] == this.curElement.type) {
        this.global = false;
        this.activeType = this.tagname[key]
        this[key] = true;
      }
    }
  },
  methods: {
    elementDel() {
      this.$emit("elementClose", {});
    },
    hoverTable(el) {
      if (!this.isOpenTable) {
        let curNode = el.parentNode.parentNode;
        let tableBlock = curNode.querySelectorAll(".table__block");
        var curIndex;
        for (let i = 0; i < tableBlock.length; i++) {
          if (tableBlock[i] == el.parentNode) {
            this.td = el.id.split("-")[1];
            this.tr = i + 1;
            curIndex = i;
          }
          let tableElement = tableBlock[i].querySelectorAll(".table__element");
          for (let j = 0; j < tableElement.length; j++) {
            tableElement[j].classList.remove("active");
          }
        }

        for (let i = 0; i <= curIndex; i++) {
          let tableElement = tableBlock[i].querySelectorAll(".table__element");
          for (let j = 0; j < tableElement.length; j++) {
            let index = tableElement[j].id.split("-")[1];
            if (index <= el.id.split("-")[1])
              tableElement[j].classList.add("active");
            else tableElement[j].classList.remove("active");
          }
        }
      }
    },
    unhover(el) {
      let tableElement = el.querySelectorAll(".table__element");
      for (let j = 0; j < tableElement.length; j++) {
        tableElement[j].classList.remove("active");
      }
    },
    createTable() {
      this.isOpenTable = true;
    },
    setActive(el) {
      this[el] = !this[el];
      let elAr = [
        "isActiveList",
        "isActiveTable",
        "isActivePersTable",
        "isActiveButton",
        "isActive",
        "isActiveImage",
        "isActiveGraph",
      ];
      for (let i = 0; i < elAr.length; i++) {
        if (elAr[i] !== el) this[elAr[i]] = false;
      }
    },
    hide(el) {
      this[el] = true;
      this.global = false;
      this.activeType = this.tagname[el];
    },
  },
  components: {
    Title,
    SubTitle,
    TextArea,
    Image,
    PieCharts,
    BulletList,
    NumberedList,
    ImageList,
    Table,
    Button,
    TableGraph,
  },
};
</script>
